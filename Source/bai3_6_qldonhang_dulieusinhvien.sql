CREATE DATABASE QLDH_3_6;
GO
USE QLDH;
GO

-- Bảng NHACUNGCAP
CREATE TABLE NHACUNGCAP (
    MANCC VARCHAR(10) PRIMARY KEY,
    TENNCC NVARCHAR(100),
    DCHI NVARCHAR(200),
    DTHOAI VARCHAR(20)
);

-- Bảng MATHANG
CREATE TABLE MATHANG (
    MAMH VARCHAR(10) PRIMARY KEY,
    TENMH NVARCHAR(100),
    DVT NVARCHAR(20),
    QUYCACH NVARCHAR(20),
    SLTON INT CHECK (SLTON > 0),
    DG MONEY
);

-- Bảng CUNGUNG
CREATE TABLE CUNGUNG (
    MANCC VARCHAR(10),
    MAMH VARCHAR(10),
    PRIMARY KEY (MANCC, MAMH),
    FOREIGN KEY (MANCC) REFERENCES NHACUNGCAP(MANCC),
    FOREIGN KEY (MAMH) REFERENCES MATHANG(MAMH)
);

-- Bảng DATHANG
CREATE TABLE DATHANG (
    SODH VARCHAR(10) PRIMARY KEY,
    NGAYDH DATE,
    MANCC VARCHAR(10),
    SL_MATHANG INT,
    GHICHU NVARCHAR(200),
    THANHTIEN MONEY,
    FOREIGN KEY (MANCC) REFERENCES NHACUNGCAP(MANCC)
);

-- Bảng CTDH
CREATE TABLE CTDH (
    SODH VARCHAR(10),
    MAMH VARCHAR(10),
    SLDAT INT,
    DGDAT MONEY,
    PRIMARY KEY (SODH, MAMH),
    FOREIGN KEY (SODH) REFERENCES DATHANG(SODH),
    FOREIGN KEY (MAMH) REFERENCES MATHANG(MAMH)
);

-- Bảng GIAOHANG
CREATE TABLE GIAOHANG (
    SOGH VARCHAR(10) PRIMARY KEY,
    NGAYGH DATE,
    SODH VARCHAR(10),
    FOREIGN KEY (SODH) REFERENCES DATHANG(SODH)
);

-- Thêm cột THANHTIEN vào bảng GIAOHANG
ALTER TABLE GIAOHANG
ADD THANHTIEN MONEY;

-- Bảng CTGH
CREATE TABLE CTGH (
    SOGH VARCHAR(10),
    MAMH VARCHAR(10),
    SLGIAO INT,
    PRIMARY KEY (SOGH, MAMH),
    FOREIGN KEY (SOGH) REFERENCES GIAOHANG(SOGH),
    FOREIGN KEY (MAMH) REFERENCES MATHANG(MAMH)
);

